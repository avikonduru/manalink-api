const mongoose = require('mongoose');

const ClientSchema = mongoose.Schema({
	first_name: {
		type: String,
		required: true,
	},
	last_name: {
		type: String,
		required: true,
	},
	email: {
		type: String,
		required: true,
		unique: true,
	},
	password: {
		type: String,
		required: true,
	},
	date: {
		type: Date,
		default: Date.now,
	},
	secret: {
		type: String,
		required: true,
	},
});

module.exports = mongoose.model('client', ClientSchema);
